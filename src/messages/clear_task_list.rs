use super::{Request, RequestResponseType, Response, HEADER};
use bincode::{Decode, Encode};

#[derive(Encode, Request)]
pub struct ClearTaskListRequest {
    header: u8,
    message_type: u8,
    _unused: u16,
    device_id: u32,
    magic_word: u32,
}

impl ClearTaskListRequest {
    pub fn new(device_id: u32, magic_word: u32) -> Self {
        ClearTaskListRequest {
            header: HEADER,
            message_type: RequestResponseType::ClearTaskList.into(),
            _unused: 0,
            device_id,
            magic_word,
        }
    }
}

#[test]
fn clear_task_list_request_to_bytes() {
    let expected = [
        0x17, 0xa6, 0x00, 0x00, 0x2d, 0x55, 0x39, 0x19, 0x55, 0xaa, 0xaa, 0x55, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    ];

    let r = ClearTaskListRequest::new(423187757, 0x55aaaa55);

    let actual = r.to_bytes();
    assert_eq!(expected, actual);
}

#[derive(Decode, Response, Debug)]
pub struct ClearTaskListResponse {
    pub header: u8,
    pub message_type: u8,
    _unused: u16,
    pub device_id: u32,
    pub success: bool,
}

#[test]
fn clear_task_list_response_from_bytes() {
    let bytes: [u8; 64] = [
        0x17, 0xa6, 0x00, 0x00, 0x2d, 0x55, 0x39, 0x19, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    ];

    let r = ClearTaskListResponse::from_bytes(&bytes).unwrap();
    assert_eq!(r.message_type, RequestResponseType::ClearTaskList.into());
    assert_eq!(r.device_id, 423187757);
    assert!(r.success);
}
